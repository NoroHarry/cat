function mouad_youssef() {

    var tsawarr = document.querySelectorAll('.imgbg')

    for (let i = 0; i < tsawarr.length; i++) {
        tsawarr[i].src = images_des_produits[i]
        console.log('done')
     }
    
}

window.onload = function(){
    mouad_youssef();
    setTimeout(function(){
        document.body.style.opacity = 1;
        document.body.style.filter = 'none'
    } , 500)
    setTimeout(function(){
        for (let i = 0; i < images_des_produits.length; i++) {
            let imageElement = document.getElementById(`image${i + 1}`);
            if (imageElement) {
                imageElement.style.opacity = 1;
                imageElement.style.filter = 'none' ;
            }
        }
    } , 2000)



    setTimeout(function(){
        let buttons = document.querySelectorAll('.button')
        buttons.forEach(button => {
        button.style.filter = 'none';
    });

    } , 3000)

    
    setTimeout(function(){
        
        if (smartphone) {
            smartphone.style.backgroundSize = '540px';
            smartphone.style.backgroundPositionX = '-5px';
            
        } else {
            console.error("L'élément n'a pas été trouvé.");
        }
        
        
    }, 3000);

    setTimeout(function(){
        for (let i = 0; i < images_des_produits.length; i++) {
            let imageElement = document.getElementById(`image${i + 1}`);
            if (imageElement) {
                imageElement.style.transition = '0.3s';
            }

        }
    } , 4000)

    
    
}



